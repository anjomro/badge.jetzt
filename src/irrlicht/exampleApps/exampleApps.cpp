    void appBlink() {
        display::println("Blinking LED...");
        display::display();

        for (int i = 0; i < 5; i++) {
            led::set_r(100, 0, 0);
            delay(250);
            led::set_r(0, 100, 0);
            delay(250);
            led::set_r(0, 0, 100);
            delay(250);
            led::set_r(0, 0, 0);
            
            led::set_l(100, 0, 0);
            delay(250);
            led::set_l(0, 100, 0);
            delay(250);
            led::set_l(0, 0, 100);
            delay(250);
            led::set_l(0, 0, 0);
        }
    }

